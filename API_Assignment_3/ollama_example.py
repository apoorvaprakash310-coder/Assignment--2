# Career related simple chatbot

question = input("Type your question: ").lower()

if "technology" in question:
    print("Technology helps in many careers like software developer, data scientist, and engineer.")
elif "career" in question:
    print("A good career needs skills, learning, and hard work.")
elif "job" in question:
    print("Technology jobs are in high demand in today's world.")
elif "future" in question:
    print("The future is bright with technology like AI and robotics.")
else:
    print("Sorry, I don't understand your question.")